def get_image_analysis_points() -> List[Dict[str, Any]]:
    return IMAGE_ANALYSIS_POINTS

def get_image_analysis_schema() -> Dict[str, Any]:
    return IMAGE_ANALYSIS_SCHEMA

def flatten_analysis_points(points: Optional[List[Dict[str, Any]]] = None) -> List[str]:
    source = points if points is not None else IMAGE_ANALYSIS_POINTS
    flat_points: List[str] = []
    for section in source:
        section_name = section.get("section", "قسم غير معروف")
        for point in section.get("points", []):
            flat_points.append(f"{section_name}: {point}")
    return flat_points

def format_analysis_points_for_prompt(points: Optional[List[Dict[str, Any]]] = None) -> str:
    source = points if points is not None else IMAGE_ANALYSIS_POINTS
    blocks: List[str] = []
    for section in source:
        section_name = section.get("section", "قسم غير معروف")
        section_points = section.get("points", [])
        bullets = "\n".join([f"- {p}" for p in section_points])
        blocks.append(f"### {section_name}\n{bullets}")
    return "\n\n".join(blocks)

def build_image_analysis_prompt(
    post_text: str = "",
    account_name: str = "",
    account_username: str = "",
    extra_context: str = "",
    strict_json: bool = True
) -> str:
    points_text = format_analysis_points_for_prompt()
    schema_text = json.dumps(IMAGE_ANALYSIS_SCHEMA, ensure_ascii=False, indent=2)

    identity_block = []
    if account_name:
        identity_block.append(f"- اسم الحساب: {account_name}")
    if account_username:
        identity_block.append(f"- المعرف: @{account_username}")
    if post_text:
        identity_block.append(f"- نص المنشور المرتبط: {post_text}")
    if extra_context:
        identity_block.append(f"- سياق إضافي: {extra_context}")

    identity_text = "\n".join(identity_block) if identity_block else "- لا توجد بيانات سياقية إضافية"

    prompt = f"""
أنت محلل استخباراتي متخصص في تحليل الصور المنشورة على منصة X (تويتر).

المطلوب:
قم بتحليل الصورة المرفقة بدقة وبشكل منهجي، مع التركيز على المؤشرات البصرية،
والدلالات الإعلامية، والرسائل الضمنية، ومؤشرات التلاعب أو الاستخدام الدعائي.

## البيانات المساندة
{identity_text}

## نقاط التحليل المطلوبة
{points_text}

## تعليمات مهمة
- اكتب التحليل باللغة العربية.
- كن دقيقًا ومهنيًا.
- إذا لم تكن بعض العناصر واضحة بصريًا فاذكر أنها غير مؤكدة.
- لا تجزم بمعلومات غير ظاهرة.
- ميّز بين الحقائق المرئية والتقديرات التحليلية.
- إذا ظهر نص داخل الصورة فاستخرجه قدر الإمكان.
- إذا ظهرت شعارات أو رموز أو أعلام فاذكرها وحلل دلالتها.
- قيّم احتمال التلاعب أو إعادة الاستخدام إن وجدت مؤشرات.

## صيغة الإخراج المطلوبة
{schema_text}
""".strip()

    if strict_json:
        prompt += "\n\nأعد النتيجة بصيغة JSON فقط دون أي شرح خارج JSON."
    else:
        prompt += "\n\nيمكنك تقديم النتيجة بصيغة منظمة وواضحة مع الحفاظ على نفس الحقول."

    return prompt

